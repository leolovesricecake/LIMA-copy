import argparse

import os
import json
import numpy as np

from tqdm import tqdm

from sklearn import metrics


def _collect_relative_and_basename_index(root_dir: str, suffix: str):
    rel_index = {}
    base_index = {}
    for cur_root, _, files in os.walk(root_dir):
        for name in files:
            if not name.endswith(suffix):
                continue
            file_path = os.path.join(cur_root, name)
            rel_path = os.path.relpath(file_path, root_dir).replace("\\", "/")
            rel_no_ext, _ = os.path.splitext(rel_path)
            rel_index[rel_no_ext] = file_path

            base_no_ext, _ = os.path.splitext(name)
            base_index.setdefault(base_no_ext, []).append(file_path)
    return rel_index, base_index


def _iter_recursive_json_npy_pairs(json_root: str, npy_root: str):
    pairs = []
    if not (os.path.isdir(json_root) and os.path.isdir(npy_root)):
        return pairs

    class_ids = sorted(set(os.listdir(json_root)) & set(os.listdir(npy_root)))
    for class_id in class_ids:
        json_class_root = os.path.join(json_root, class_id)
        npy_class_root = os.path.join(npy_root, class_id)
        if not (os.path.isdir(json_class_root) and os.path.isdir(npy_class_root)):
            continue

        npy_rel_index, npy_base_index = _collect_relative_and_basename_index(
            npy_class_root, ".npy"
        )

        for cur_root, _, files in os.walk(json_class_root):
            for name in files:
                if not name.endswith(".json"):
                    continue
                json_file_path = os.path.join(cur_root, name)
                rel_json = os.path.relpath(json_file_path, json_class_root)
                rel_base, _ = os.path.splitext(rel_json)
                rel_base = rel_base.replace("\\", "/")
                npy_file_path = npy_rel_index.get(rel_base)

                # Fallback for datasets where json/npy extra subfolders are not strictly aligned:
                # if class-local basename is unique, pair by basename.
                if npy_file_path is None:
                    base_no_ext, _ = os.path.splitext(name)
                    npy_candidates = npy_base_index.get(base_no_ext, [])
                    if len(npy_candidates) == 1:
                        npy_file_path = npy_candidates[0]
                    else:
                        continue
                pairs.append((class_id, json_file_path, npy_file_path))
    return pairs


def parse_args():
    parser = argparse.ArgumentParser(description='Faithfulness Metric')
    parser.add_argument('--explanation-dir', 
                        type=str, 
                        default='submodular_results_iclr_baseline/imagenet-languagebind-false/grad-10x10-4',
                        help='Save path for saliency maps generated by our methods.')
    args = parser.parse_args()
    return args

def main(args):
    print(args.explanation_dir)

    insertion_aucs = []
    deletion_aucs = []
    sample_count = 0
    class_folder_count = 0
    format_name = ""

    json_root_file = os.path.join(args.explanation_dir, "json")
    npy_root_file = os.path.join(args.explanation_dir, "npy")

    # Format-1 (ours): explanation_dir/{json,npy}/class_id/*.json|*.npy
    if os.path.isdir(json_root_file) and os.path.isdir(npy_root_file):
        format_name = "format-1(ours)"
        pairs = _iter_recursive_json_npy_pairs(json_root_file, npy_root_file)
        class_folder_count = len(set([x[0] for x in pairs]))
        for _, json_file_path, npy_file_path in tqdm(pairs):
            try:
                with open(json_file_path, 'r', encoding='utf-8') as f:
                    saved_json_file = json.load(f)
                submodular_image_set = np.load(npy_file_path)
            except Exception:
                continue

            if "consistency_score" not in saved_json_file or "collaboration_score" not in saved_json_file:
                continue

            insertion_area = []
            deletion_area = []
            image = submodular_image_set.sum(0)

            insertion_ours_image = image.copy() - image.copy()
            deletion_ours_image = image.copy()

            insertion_area.append(
                (insertion_ours_image.sum(-1)!=0).sum() / (image.shape[0] * image.shape[1]))
            deletion_area.append(
                (deletion_ours_image.sum(-1)!=0).sum() / (image.shape[0] * image.shape[1]))

            for smdl_sub_mask in submodular_image_set:
                insertion_ours_image = insertion_ours_image + smdl_sub_mask
                deletion_ours_image = image - insertion_ours_image
                insertion_area.append(
                    (insertion_ours_image.sum(-1)!=0).sum() / (image.shape[0] * image.shape[1]))
                deletion_area.append(
                    (deletion_ours_image.sum(-1)!=0).sum() / (image.shape[0] * image.shape[1]))

            insertion_score = saved_json_file["consistency_score"]
            deletion_score = saved_json_file["collaboration_score"]

            insertion_score = np.array([1 - deletion_score[-1]] + insertion_score)
            deletion_score = 1 - np.array([1 - insertion_score[-1]] + deletion_score)

            insertion_auc = metrics.auc(np.array(insertion_area), insertion_score)
            deletion_auc = metrics.auc(1-np.array(deletion_area), deletion_score)
            insertion_aucs.append(insertion_auc)
            deletion_aucs.append(deletion_auc)
            sample_count += 1
    else:
        # Format-2 (baseline): explanation_dir/*.json
        format_name = "format-2(baseline)"
        json_file_names = [f for f in os.listdir(args.explanation_dir) if f.endswith(".json")]
        if len(json_file_names) == 0:
            raise ValueError(
                "No valid explanation files found. Expect either '{json,npy}' subdirs "
                "or direct '*.json' files under explanation_dir."
            )

        for json_file_name in tqdm(json_file_names):
            json_file_path = os.path.join(args.explanation_dir, json_file_name)
            with open(json_file_path, 'r', encoding='utf-8') as f:
                saved_json_file = json.load(f)

            insertion_score = saved_json_file["consistency_score"]
            deletion_score = saved_json_file["collaboration_score"]
            steps = len(insertion_score)
            if steps == 0:
                continue

            insertion_area = np.array([i / steps for i in range(steps + 1)])
            deletion_area = np.array([1 - i / steps for i in range(steps + 1)])
            insertion_score = np.array([deletion_score[-1]] + insertion_score)
            deletion_score = np.array([insertion_score[-1]] + deletion_score)

            insertion_auc = metrics.auc(insertion_area, insertion_score)
            deletion_auc = metrics.auc(deletion_area, deletion_score)
            insertion_aucs.append(insertion_auc)
            deletion_aucs.append(deletion_auc)
            sample_count += 1

    if len(insertion_aucs) == 0 or len(deletion_aucs) == 0:
        raise ValueError("No AUC values were computed. Please check explanation files.")

    insertion_auc_score = np.array(insertion_aucs).mean()
    deletion_auc_score = np.array(deletion_aucs).mean()
    if format_name == "format-1(ours)":
        print(
            "AUC evaluated on {} samples across {} class folders ({})".format(
                sample_count, class_folder_count, format_name
            )
        )
    else:
        print("AUC evaluated on {} samples ({})".format(sample_count, format_name))
    print("Insertion AUC Score: {:.4f}\nDeletion AUC Score: {:.4f}".format(insertion_auc_score, deletion_auc_score))
    return

if __name__ == "__main__":
    args = parse_args()
    main(args)
